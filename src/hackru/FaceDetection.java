    class DaemonThread implements Runnable {

        protected volatile boolean runnable = false;
        protected volatile boolean detected = false;
        protected volatile int pushupcount = 0;
        
        @Override
        public void run() {
        	Date.setText(subject.toString());
            synchronized (this) {
                while (runnable) {
                    jProgressBar1.setStringPainted(true);
                    
                    jLabel4.setText("Set:" + (subject.getWorkout().getCurrentSet() + 1) + "//" + (subject.getWorkout().getSets() + 1));
                	System.out.println(pushupcount);
                    if (webSource.grab()) {
                        try {
                            webSource.retrieve(frame);
                            Graphics g = jPanel1.getGraphics();
                            faceDetector.detectMultiScale(frame, faceDetections);
                            
                            boolean currentlyDetected = false;
                            
                            if (faceDetections.toArray().length != 0)
                            	currentlyDetected = true;
                            else
                            	detected = false;
                            
                            if (currentlyDetected == true && detected != true)
                            {
                            	jProgressBar1.setMaximum(subject.getWorkout().getReps());
                            	pushupcount++;
                                String temp = "" + pushupcount;
                                jLabel1.setText(temp);
                                jProgressBar1.setValue(pushupcount);
                                detected = true;
                                Date.setText("" + subject.getWorkout().getReps());
                            }
                            
                            for (Rect rect : faceDetections.toArray()) {
                                
                                Core.rectangle(frame, new Point(rect.x, rect.y), new Point(rect.x + rect.width, rect.y + rect.height),
                                        new Scalar(0, 255,0));
                            }
                            Highgui.imencode(".bmp", frame, mem);
                            Image im = ImageIO.read(new ByteArrayInputStream(mem.toArray()));
                            BufferedImage buff = (BufferedImage) im;
                            if (g.drawImage(buff, 0, 0, getWidth(), getHeight()+0 , 0, 0, buff.getWidth(), buff.getHeight(), null)) {
                                if (runnable == false) {
                                    System.out.println("Paused ..... ");
                                    this.wait();
                                }
                            }
                            
                            if(pushupcount == subject.getWorkout().getReps() && subject.getWorkout().getCurrentSet() == subject.getWorkout().getSets())
                            {
                            	JOptionPane.showMessageDialog(null, "You finished today's workout!", "Congratulations!", 1);
                            	subject.incDay();
                            	subject.saveProfile();
                            	System.exit(0);
                            }
                            
                            else if(pushupcount == subject.getWorkout().getReps())
                            {
                            	jProgressBar1.setMaximum(60);
                            	if(pushupcount > subject.getMax())
                            		subject.setMax(pushupcount);
                            	for(int i = 0; i < 60; i++){
                            		jLabel1.setText("REST " + (60 - i));                            		
                            		jProgressBar1.setValue(i);
                            		try {
                            			  Thread.sleep(1000);
                            			} catch (InterruptedException ie) {}
                            	pushupcount = 0;
                            	}
                            	
                            	subject.getWorkout().completeSet();
                            	
                            }
                        } catch (Exception ex) {
                            System.out.println("Error");
                        }
                    }
                }
            }
        }
    }

